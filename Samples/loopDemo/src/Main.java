public class Main {
    public static void main(String[] args) {

        int i;
        for( i = 1; i<10; i++) {
            System.out.println(i);
        }
        System.out.println("For Loop is finished");

        i = 1;
        while(i<10) {

            System.out.println(i);
            i++;

        }
        System.out.println("While Loop is finished");
    }
}